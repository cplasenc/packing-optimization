    private void janadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_janadirActionPerformed
        DefaultTableModel model=(DefaultTableModel)tabla3.getModel();
        DatosProductos d=new DatosProductos();
        List<DatosProductos> ls=d.recogerDatosProductos();
        String descripcion=(DescripcionProducto.getSelectedItem()).toString();
        
        Object cantidad=Cantidad.getSelectedItem();
        Object arr[]=new Object[5];
        arr[4]=cantidad;
        for(int i=0; i<ls.size(); i++){
            if(ls.get(i).descripcion.equals(descripcion)){
                arr[0]=descripcion;
                arr[1]=ls.get(i).ancho;
                arr[2]=ls.get(i).alto;
                arr[3]=ls.get(i).largo;
                break;
            }
        }
        model.addRow(arr);
        

    }//GEN-LAST:event_janadirActionPerformed
    
    
    
    
