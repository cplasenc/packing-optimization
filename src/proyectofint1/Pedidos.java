    private void guardarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarPActionPerformed
    DefaultTableModel model=(DefaultTableModel)tabla3.getModel();
    
    int col=model.getColumnCount();
    int fila=model.getRowCount();
    Random r=new Random();
    int id=(r.nextInt());
    for(int i=0; i<fila; i++){
        DatosProductos d=new DatosProductos();
        for(int j=0; j<col; j++){   
            
            if(j==0){
                d.descripcion=model.getValueAt(i, j).toString();
            }
            else if(j==1){
                d.ancho=Integer.parseInt(model.getValueAt(i, j).toString());
            }
            else if(j==2){
                d.alto=Integer.parseInt(model.getValueAt(i, j).toString());
            }
            else if(j==3){
                d.largo=Integer.parseInt(model.getValueAt(i, j).toString());
            }
            else if(j==4) {
                d.cantidad=Integer.parseInt(model.getValueAt(i, j).toString());
            }
 
        }
         int [] arr= d.TamanoMinCaja(d);
         String caja=Integer.toString(arr[0])+":"+Integer.toString(arr[1])+":"+Integer.toString(arr[2])+":";
         d.EscribirPedido("ID-no."+Integer.toString(id),caja,Integer.toString(arr[3]));
    }
    mesg.setText("Pedido generado");
    }//GEN-LAST:event_guardarPActionPerformed

